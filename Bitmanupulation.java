public class Bitmanupulation {
    public static void main(String[] args) {
        System.out.println("print binray AND "+(5&6));
        System.err.println("print binary OR "+(5|6));
        System.err.println("print binary XOR "+(5^6));
        System.err.println("print binary one's componenet : "+(~5));
        System.err.println("print binary left shift "+(5<<2));
        System.err.println("print binary Right shift "+(6>>1));
    }
    
}
